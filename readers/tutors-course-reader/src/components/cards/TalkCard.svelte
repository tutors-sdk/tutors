<script lang="js">
  import Icon from "tutors-reader-lib/src/iconography/Icon.svelte";
  import { Clock } from "svelte-loading-spinners";
  import FileSaver from "file-saver";
  import { onDestroy, tick } from "svelte";

  import * as pdfjs from "pdfjs-dist/build/pdf.js";
  import pdfjsWorker from "pdfjs-dist/build/pdf.worker.entry";

  export let url;
  export let scale = 1.8;
  export let pageNum = 1; //must be number
  export let lo = null;

  url = lo.pdf;
  let status = "";

  let canvas;
  let page_num = 0;
  let pageCount = 0;
  let pdfDoc = null;
  let pageRendering = false;
  let pageNumPending = null;
  let rotation = 0;
  let totalPage = 0;
  let interval;
  let secondInterval;

  let pages = [];

  const renderPage = (num) => {
    pageRendering = true;

    pdfDoc.getPage(num).then(function(page) {
      let viewport = page.getViewport({ scale: scale, rotation: rotation });
      const canvasContext = canvas.getContext("2d");
      canvas.height = viewport.height;
      canvas.width = viewport.width;

      let renderContext = {
        canvasContext,
        viewport
      };
      let renderTask = page.render(renderContext);

      // Wait for rendering to finish
      renderTask.promise.then(function() {
        pageRendering = false;
        if (pageNumPending !== null) {
          // New page rendering is pending
          // renderPage(pageNumPending);
          if (pageNum < pdfDoc.totalPage) {
            pages[pageNum] = canvas;
            pageNum++;
            pdfDoc.getPage(pageNum).then(renderPage);
          } else {
            for (let i = 1; i < pages.length; i++) {
              canvas.appendChild(pages[i]);
            }
          }
          pageNumPending = null;
        }
      });
    });
  };

  const queueRenderPage = (num) => {
    if (pageRendering) {
      pageNumPending = num;
    } else {
      renderPage(num);
    }
  };

  const onPrevPage = () => {
    if (pageNum <= 1) {
      return;
    }
    pageNum--;
    queueRenderPage(pageNum);
  };

  const onNextPage = () => {
    if (pageNum >= pdfDoc.numPages) {
      return;
    }
    pageNum++;
    queueRenderPage(pageNum);
  };

  const clockwiseRotate = () => {
    rotation = rotation + 90;
    queueRenderPage(pageNum);
  };

  const downloadPdf = () => {
    let fileName = url.substring(url.lastIndexOf("/") + 1);
    FileSaver.saveAs(url, fileName);
  };

  const initialLoad = async () => {
    window.addEventListener("keydown", keypressInput);
    let loadingTask = pdfjs.getDocument({ url });
    loadingTask.promise
      .then(async function(pdfDoc_) {
        pdfDoc = pdfDoc_;
        await tick();
        pageCount = pdfDoc.numPages;
        totalPage = pageCount;
        renderPage(pageNum);
      })
      .catch(function(error) {
      });
  };
  initialLoad();

  onDestroy(() => {
    clearInterval(interval);
    clearInterval(secondInterval);
    window.removeEventListener("keypress", keypressInput);
  });

  function keypressInput(e) {
    if (e.key === "ArrowRight") {
      e.preventDefault();
      onNextPage();
    } else if (e.key === "ArrowLeft") {
      e.preventDefault();
      onPrevPage();
    }
  }

</script>


{#if pdfDoc}
  <div class="talkcard-container">
    <div class="flex justify-between items-center mx-2">
      <div class="text-sm">
        {pageNum} of {pdfDoc.numPages}
      </div>
      <div>
        <button on:click={onPrevPage}>
          <Icon button="true" type="left" tipPos="tooltip-bottom" toolTip="Previous Slide"/>
        </button>
        <button on:click={onNextPage}>
          <Icon button="true" type="right" tipPos="tooltip-bottom" toolTip="Next Slide"/>
        </button>
        <button on:click={clockwiseRotate} >
          <Icon button="true" type="rotate" tipPos="tooltip-bottom" toolTip="Rotate 90 Degrees"/>
        </button>
        <button on:click={downloadPdf} >
          <Icon type="download" button="true" toolTip="download" tipPos="tooltip-bottom" />
        </button>
        <Icon button="true" link={lo.pdf} type="fullScreen" toolTip="Full Screen" tipPos="tooltip-bottom" target="_blank" />
      </div>
    </div>
    <canvas class="w-full 2xl:w-4/5 mx-auto" bind:this={canvas} />
  </div>
{:else}
  <div class="flex flex-col justify-center items-center mt-28">
    <Clock size="280" color="#FF3E00" unit="px" />
  </div>
{/if}
